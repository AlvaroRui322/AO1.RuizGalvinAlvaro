let numero = 2
let numero2 = 4
let resultado
//Sumando
resultado = numero + numero2
console.log(`${numero} + ${numero2} = ${resultado}`)
//Restando
resultado = numero - numero2
console.log(`${numero} - ${numero2} = ${resultado}`)
//Multiplicando
resultado = numero * numero2
console.log(`${numero} x ${numero2} = ${resultado}`)
//Dividiendo
resultado = numero / numero2
console.log(`${numero} / ${numero2} = ${resultado}`)
//Incremento
resultado++
console.log(resultado)